    private void btnAtacarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtacarseActionPerformed
        // Si el jugador 1 no ha generado un combo, el ataque central no se realizará
        if (panelJugador1Real != null && panelJugador1Real.tieneComboGenerado()) {
            // Guardar marca de tiempo antes de ejecutar el combo. Si durante la
            // ejecución se reinicia el juego (por ejemplo por el diálogo de
            // ganador), la marca de inicialización cambiará y evitaremos que
            // J2 ejecute su ataque en la nueva partida.
            long antesInit = controlador.getUltimaInicializacionMillis();

            // Ejecutar el combo generado por J1
            panelJugador1Real.usarCombo();

            // Si no hubo reinicio durante la ejecución y no hay ganador,
            // permitir que J2 responda aleatoriamente.
            boolean reinicioDuranteCombo = controlador.getUltimaInicializacionMillis() != antesInit;
            if (!reinicioDuranteCombo && !controlador.hayGanador()) {
                controlador.jugador2AtacaAleatorio();
            }

            actualizarVista();
        } else {
            JOptionPane.showMessageDialog(this, "El Jugador 1 no ha seleccionado un combo aún.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAtacarseActionPerformed


    public void mostrarGanador(com.mycompany.strategyy.model.Jugador ganador) {
        String mensaje = ganador.getNombre() + " ha ganado\nVida restante: " + ganador.getVida();
        int opcion = JOptionPane.showConfirmDialog(
            this,
            mensaje + "\n¿Deseas jugar otra vez?",
            "Fin del juego",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE
        );

        if (opcion == JOptionPane.YES_OPTION) {
            // Reiniciar el juego y actualizar la UI
            controlador.inicializarJuego();
            inicializarPaneles();
            actualizarVista();
        } else {
            // Sólo mostrar el mensaje de que el juego termino 
            JOptionPane.showMessageDialog(this, mensaje, "Fin del juego", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    // Actualizar la vista completa
    public void actualizarVista() {
        panelJugador1Real.actualizar();
        panelJugador2Real.actualizar();
    }
    
    // Inicializar los paneles
    private void inicializarPaneles() {
        // Limpiar los paneles vacíos que se generan por NetBeans
        plnJugador1.removeAll();
        plnJugador2.removeAll();
        
        // Se crean paneles para cada jugador con su respectivo modelo 
        panelJugador1Real = new PanelJugador(controlador.getJugador1(), true, this);
        panelJugador2Real = new PanelJugador(controlador.getJugador2(), false, this);
        
        plnJugador1.setOpaque(true);
        plnJugador2.setOpaque(true);
        panelJugador1Real.setOpaque(true);
        panelJugador2Real.setOpaque(true);
        
        
        
        // Configurar layout y agregar los paneles
        plnJugador1.setLayout(new BorderLayout());
        plnJugador1.add(panelJugador1Real, BorderLayout.CENTER);
        
        plnJugador2.setLayout(new BorderLayout());
        plnJugador2.add(panelJugador2Real, BorderLayout.CENTER);
        
        // Refrescar los paneles
        plnJugador1.revalidate();
        plnJugador1.repaint();
        plnJugador2.revalidate();
        plnJugador2.repaint();
    }
    
  
